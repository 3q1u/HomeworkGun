void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
    if (GPIO_Pin == BUTTON_Pin && !HAL_DCMI_GetState(&hdcmi) == HAL_DCMI_STATE_READY && !HAL_UART_GetState(&huart2)) {
      HAL_DCMI_Start_DMA(&hdcmi, DCMI_MODE_SNAPSHOT, (uint32_t)FRAME_BUFFER, IMAGE_HEIGHT*IMAGE_WIDTH*2/4);
    }
}

void DCMI_CpltCallback(DCMI_HandleTypeDef *hdcmi_) {
    if (hdcmi_->Instance == DCMI) {
        HAL_UART_Transmit_DMA(&huart2, (uint8_t *)FRAME_BUFFER, IMAGE_HEIGHT*IMAGE_WIDTH*2);
    }
}

void HAL_UART_TxCpltCallback(UART_HandleTypeDef *huart) {
    if (huart->Instance == USART2) {
        HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_15);
    }
}
